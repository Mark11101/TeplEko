export default (index: number): string => {

  switch (index) {

    case 1:
      return 'Понедельник'
    case 2:
      return 'Вторник'
    case 3:
      return 'Среда'
    case 4:
      return 'Четверг'
    case 5:
      return 'Пятница'
    case 6:
      return 'Cуббота'
    case 7:
      return 'Воскресенье'
    default:
      return 'Понедельник'
  }
}
